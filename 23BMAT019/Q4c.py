n = input("Enter a digit: ")
msg = "Invalid Digit"

if (n == "0"):
    msg = "ZERO"

elif (n == "1"):
    msg = "ONE"

elif (n == "2"):
    msg = "TWO"

elif (n == "3"):
    msg = "THREE"

elif (n == "4"):
    msg = "FOUR"

elif (n == "5"):
    msg = "FIVE"

elif (n == "6"):
    msg = "SIX"

elif (n == "7"):
    msg = "SEVEN"

elif (n == "8"):
    msg = "EIGHT"

elif (n == "9"):
    msg = "NINE"

print(msg)
